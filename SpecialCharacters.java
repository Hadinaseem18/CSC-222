/**
 * This class demonstrates the following: 
 * special characters like the square root symbol formatting
 * the exponent value as a superscript
 *
 * @author shriyanavil
 * @version Feb 1, 2025
 *
 */
public class SpecialCharacters {

    /**
     * main method
     *
     * @param args None
     */
    public static void main(String[] args) {
        System.out.println("Displaying the square root symbol: ");
        System.out.println("\u221A" + 25);

        System.out.println("Displaying an exponent as a superscript: ");
        System.out.println("\u221A" );
    }
}
